
public class Productos {

	String referencia ;
	String stock ;
	String tipoProducto ;
	String marca ;
	String talla ;
	String precio ;
	
	
	public Productos(String referencia, String stock, String tipoProducto, String marca, String talla, String precio) {
		super();
		this.referencia = referencia;
		this.stock = stock;
		this.tipoProducto = tipoProducto;
		this.marca = marca;
		this.talla = talla;
		this.precio = precio;
	}
	


}
