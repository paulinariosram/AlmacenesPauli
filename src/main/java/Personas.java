
public class Personas {

	
	String nombre ;
	String apellido ;
	String tipoDoc ;
	String numDoc ;
	
	
	public Personas(String nombre, String apellido, String tipoDoc, String numDoc) {
		super();
		this.nombre = nombre;
		this.apellido = apellido;
		this.tipoDoc = tipoDoc;
		this.numDoc = numDoc;
	}
	
	
	
}
